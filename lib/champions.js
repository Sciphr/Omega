// Search function for champions
export function searchChampions(query, excludeIds = []) {
  if (!query) return CHAMPIONS.filter(c => !excludeIds.includes(c.id))

  const lowerQuery = query.toLowerCase()
  return CHAMPIONS.filter(champion =>
    champion.name.toLowerCase().includes(lowerQuery) &&
    !excludeIds.includes(champion.id)
  )
}

// Get champion by ID
export function getChampion(id) {
  return CHAMPIONS.find(c => c.id === id)
}

// Get champions by role
export function getChampionsByRole(role) {
  return CHAMPIONS.filter(c => c.role === role)
}